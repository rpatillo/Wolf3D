#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		l;
	char	*s2;

	i = 0;
	if (s1 == NULL)
		return (NULL);
	l = ft_strlen(s1);
	s2 = (char *)malloc(sizeof(s1) * (l + 1));
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
